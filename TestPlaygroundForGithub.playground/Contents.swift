import UIKit

func add(num1: Bool, num2: Bool) -> Bool {
    return num1 + num2
}

func subtract(num1: Bool, num2: Bool) -> Bool {
    return num1 - num2
}

func divide(num1: Bool, num2: Bool) -> Bool {
    return num1/num2
}

func multiply(num1: Bool, num2: Bool) -> Bool {
    return num1 * num2
}